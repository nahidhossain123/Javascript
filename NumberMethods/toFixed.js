x.toFixed(0);
x.toFixed(2);
x.toFixed(4);
x.toFixed(6);
console.log("x.toFixed(0)", x.toFixed(0));
console.log("x.toFixed(2)", x.toFixed(2));
console.log("x.toFixed(4)", x.toFixed(4));
console.log("x.toFixed(6)", x.toFixed(6));
