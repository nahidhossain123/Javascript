//toExponential() method returns a string, with the number rounded and written using exponential notation.
let x = 9.656;

x.toExponential();
x.toExponential(2);
x.toExponential(4);
x.toExponential(6);
console.log("toExponential()", x.toExponential());
console.log("toExponential(2)", x.toExponential(2));
console.log("toExponential(4)", x.toExponential(4));
console.log("toExponential(6)", x.toExponential(6));
