//trim()--->remove whitespace from both start and end;
let text = "      Hello World!      ";
let trim = text.trim();
console.log("trim() string=", trim);
//trimStart()--->remove whitespace from start;
let trimStart = text.trimStart();
console.log("trimStart() string=", trimStart, "'");

//trimEnd()--->remove whitespace from start;
let textEnd = text.trimEnd();
console.log("trimEnd() string='", textEnd);
